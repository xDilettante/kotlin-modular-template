// ---------------------------------------------------------
// Управление подключением плагинов Gradle
// ---------------------------------------------------------
pluginManagement {
    repositories {
        gradlePluginPortal()    // Официальный репозиторий Gradle для плагинов
        mavenCentral()          // Центральный репозиторий Maven
        google()                // Репозиторий Google (нужен для Android и некоторых Kotlin плагинов)
    }
}

// ---------------------------------------------------------
// Управление зависимостями и версиями
// ---------------------------------------------------------
dependencyResolutionManagement {
    versionCatalogs {
        create("libs") {
            // Подключение внешнего файла со всеми версиями библиотек
            // libs.versions.toml — централизованный файл, где хранятся версии зависимостей
            from(files("libs.versions.toml"))
        }
    }
}

// ---------------------------------------------------------
// Подключение плагина для автоматического выбора JDK через Foojay API
// ---------------------------------------------------------
plugins {
    // Плагин Foojay Toolchain Resolver позволяет Gradle автоматически
    // находить и устанавливать нужную версию JDK, указанную в build.gradle
    id("org.gradle.toolchains.foojay-resolver-convention") version "1.0.0"
}

// ---------------------------------------------------------
// Общие настройки проекта
// ---------------------------------------------------------

// Имя корневого проекта
rootProject.name = "xProject"

// Добавление модулей (подпроектов) в сборку
include("xApp")     // Пример приложения для тестов
include("xLib")    // Библиотека или модуль API для Telegram-бота
